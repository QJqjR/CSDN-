package CSDN.angel.model;

public class Job {
	private int id;
	private String comLogo;
	private String comPosition;
	private String comName;
	private String comSalary;
	private String comTime;
	private String comEducation;
	private String comAdress;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getComLogo() {
		return comLogo;
	}
	public void setComLogo(String comLogo) {
		this.comLogo = comLogo;
	}
	public String getComPosition() {
		return comPosition;
	}
	public void setComPosition(String comPosition) {
		this.comPosition = comPosition;
	}
	public String getComName() {
		return comName;
	}
	public void setComName(String comName) {
		this.comName = comName;
	}
	public String getComSalary() {
		return comSalary;
	}
	public void setComSalary(String comSalary) {
		this.comSalary = comSalary;
	}
	public String getComTime() {
		return comTime;
	}
	public void setComTime(String comTime) {
		this.comTime = comTime;
	}
	public String getComEducation() {
		return comEducation;
	}
	public void setComEducation(String comEducation) {
		this.comEducation = comEducation;
	}
	public String getComAdress() {
		return comAdress;
	}
	public void setComAdress(String comAdress) {
		this.comAdress = comAdress;
	}
	
	
}
